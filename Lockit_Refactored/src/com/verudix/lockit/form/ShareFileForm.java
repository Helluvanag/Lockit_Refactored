package com.verudix.lockit.form;

public class ShareFileForm {

	private String sharedemailid;
	private String sharedfileid;
	private String expiredate;
	private String downloadprop;
	private String sharingprop;
	private String printprop;
	
	public String getSharedemailid() {
		return sharedemailid;
	}
	public void setSharedemailid(String sharedemailid) {
		this.sharedemailid = sharedemailid;
	}
	public String getSharedfileid() {
		return sharedfileid;
	}
	public void setSharedfileid(String sharedfileid) {
		this.sharedfileid = sharedfileid;
	}
	public String getExpiredate() {
		return expiredate;
	}
	public void setExpiredate(String expiredate) {
		this.expiredate = expiredate;
	}
	public String getDownloadprop() {
		return downloadprop;
	}
	public void setDownloadprop(String downloadprop) {
		this.downloadprop = downloadprop;
	}
	public String getSharingprop() {
		return sharingprop;
	}
	public void setSharingprop(String sharingprop) {
		this.sharingprop = sharingprop;
	}
	public String getPrintprop() {
		return printprop;
	}
	public void setPrintprop(String printprop) {
		this.printprop = printprop;
	}

}
